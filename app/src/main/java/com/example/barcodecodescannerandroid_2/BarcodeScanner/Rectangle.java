package com.example.barcodecodescannerandroid_2.BarcodeScanner;

public class Rectangle {
    public int row_1;
    public int row_2;
    public int col_1;
    public int col_2;

    public Rectangle(int row_1, int row_2, int col_1, int col_2) {
        this.row_1 = row_1;
        this.row_2 = row_2;
        this.col_1 = col_1;
        this.col_2 = col_2;
    }
}
